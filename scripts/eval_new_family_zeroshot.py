"""Phase A: zero-shot PRM generalization to STRUCTURALLY-NEW chain families.

The strong PRM is trained ONLY on the original families (prm_samples_train.jsonl) and
evaluated on the three new topology families (authed_injection, chained_exploit,
leak_authed_privesc) that it has NEVER seen at any structural level. This is the honest
test the saturation learning-curve could not run: does the PRM generalize to genuinely
new chain TOPOLOGIES, or only to new tokens/instances of seen topologies?

Labels on the new families come from the EXISTING destmpl oracle (no retrain), so this
measures the current pipeline's structural transfer ceiling. Metrics are on the
oracle-labeled subset only, broken out per new family, vs the majority floor.
"""

from __future__ import annotations

import os

os.environ.setdefault("KMP_DUPLICATE_LIB_OK", "TRUE")

from pathlib import Path
import argparse
import json
import sys
from collections import defaultdict
from typing import Any

import numpy as np
from sklearn.ensemble import HistGradientBoostingClassifier, HistGradientBoostingRegressor
from sklearn.feature_extraction import DictVectorizer

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT))
sys.path.insert(0, str(ROOT / "scripts"))

from honest_eval import majority_floor, pairwise_within_group  # noqa: E402
from train_prm_baseline import read_jsonl  # noqa: E402
from train_prm_strong import extract_features  # noqa: E402
from web_attack_sim.tasks import load_task_config  # noqa: E402

# The structurally-novel families held out of training for the zero-shot test.
NEW_FAMILIES = {"authed_injection", "chained_exploit", "leak_authed_privesc"}


def stem(path_str: str) -> str:
    return Path(str(path_str)).stem


def family_of(row: dict[str, Any], fam_cache: dict[str, str]) -> str:
    s = stem(row.get("task_path", ""))
    if s not in fam_cache:
        try:
            fam_cache[s] = load_task_config(ROOT / "tasks" / f"{s}.json").get("family", "?")
        except Exception:
            fam_cache[s] = "?"
    return fam_cache[s]


def eval_subset(score_pred, rank_pred, rows, idxs, eps) -> dict[str, Any]:
    if not idxs:
        return {"n": 0}
    y_true = [str(rows[i]["rank_label"]) for i in idxs]
    rec: dict[str, Any] = {"n": len(idxs), "majority_floor": majority_floor(y_true)}
    pair = pairwise_within_group(rows, list(score_pred), idxs, eps)
    rec["pairwise_accuracy"] = pair["pairwise_accuracy"]
    rec["pairwise_pairs"] = pair["pairs"]
    if rank_pred is not None:
        rec["rank_accuracy"] = float(np.mean([int(rank_pred[i] == rows[i]["rank_label"]) for i in idxs]))
        rec["rank_lift_over_floor"] = rec["rank_accuracy"] - rec["majority_floor"]
    return rec


def main() -> None:
    parser = argparse.ArgumentParser(description="Zero-shot PRM generalization to new chain families.")
    parser.add_argument("--train-input", type=Path, default=ROOT / "outputs" / "prm_samples_train.jsonl")
    parser.add_argument("--newfam-input", type=Path, default=ROOT / "outputs" / "prm_samples_newfam.jsonl")
    parser.add_argument("--report-output", type=Path, default=ROOT / "outputs" / "prm_new_family_zeroshot.json")
    parser.add_argument("--seed", type=int, default=0)
    parser.add_argument("--pairwise-eps", type=float, default=1e-6)
    args = parser.parse_args()

    train_rows = read_jsonl(args.train_input)
    new_rows = read_jsonl(args.newfam_input)

    # Enforce the zero-shot premise: train ONLY on the original families. The canonical
    # 12-family prm_samples_train now CONTAINS the 3 new families (they were folded into
    # train for coverage), so we must exclude their rows here, otherwise this is no longer
    # a held-out structural-generalization test. (Label consistency: new_rows must be
    # generated by the SAME canonical oracle as train_rows — regenerate prm_samples_newfam
    # whenever the oracle changes.)
    fam_cache_filter: dict[str, str] = {}
    n_before = len(train_rows)
    train_rows = [r for r in train_rows if family_of(r, fam_cache_filter) not in NEW_FAMILIES]
    n_excluded = n_before - len(train_rows)

    vec = DictVectorizer(sparse=True)
    x_tr = vec.fit_transform([extract_features(r) for r in train_rows]).toarray()
    x_new = vec.transform([extract_features(r) for r in new_rows]).toarray()
    y_score = np.asarray([float(r["score"]) for r in train_rows], dtype=np.float32)
    y_rank = [str(r["rank_label"]) for r in train_rows]

    score = HistGradientBoostingRegressor(random_state=args.seed, max_iter=300, learning_rate=0.08)
    score.fit(x_tr, y_score)
    score_pred = np.clip(score.predict(x_new), 0.0, 1.0)
    rank = HistGradientBoostingClassifier(random_state=args.seed, max_iter=300, learning_rate=0.08)
    rank.fit(x_tr, y_rank)
    rank_pred = list(rank.predict(x_new))

    fam_cache: dict[str, str] = {}
    oracle_idx = [i for i, r in enumerate(new_rows) if r.get("label_source") == "oracle"]
    by_family: dict[str, list[int]] = defaultdict(list)
    for i in oracle_idx:
        by_family[family_of(new_rows[i], fam_cache)].append(i)

    report: dict[str, Any] = {
        "n_train_samples": len(train_rows),
        "n_train_rows_excluded_as_new_family": n_excluded,
        "n_new_family_samples": len(new_rows),
        "n_new_family_oracle_labeled": len(oracle_idx),
        "trained_on": "original families only (new-family rows excluded from prm_samples_train)",
        "oracle_labels_from": "canonical seed-gate oracle (same oracle labels train + new families)",
        "per_new_family": {fam: eval_subset(score_pred, rank_pred, new_rows, idxs, args.pairwise_eps)
                           for fam, idxs in sorted(by_family.items())},
        "all_new_families": eval_subset(score_pred, rank_pred, new_rows, oracle_idx, args.pairwise_eps),
        "note": (
            "Pairwise/rank accuracy on the oracle-labeled subset of the NEW families, with a PRM "
            "that trained on the original families only. Compare against the in-distribution "
            "oracle-subset pairwise (~0.94) and the majority floor. A large drop = the new chain "
            "TOPOLOGIES are genuinely out of the PRM's structural coverage (more families would "
            "help); near-parity = the PRM already generalizes structurally."
        ),
    }
    args.report_output.parent.mkdir(parents=True, exist_ok=True)
    args.report_output.write_text(json.dumps(report, ensure_ascii=False, indent=2), encoding="utf-8")

    print(f"trained on {len(train_rows)} original-family samples (excluded {n_excluded} new-family rows); "
          f"new-family oracle-labeled: {len(oracle_idx)}/{len(new_rows)}")
    print(f"{'family':>22}  {'n':>4}  {'pairwise':>9}  {'rank':>6}  {'floor':>6}  {'lift':>6}")
    for fam, rec in report["per_new_family"].items():
        pa = rec.get("pairwise_accuracy")
        ra = rec.get("rank_accuracy")
        fl = rec.get("majority_floor")
        lift = rec.get("rank_lift_over_floor")
        print(f"{fam:>22}  {rec['n']:>4}  {('%.3f' % pa) if pa is not None else '  n/a ':>9}  "
              f"{('%.3f' % ra) if ra is not None else ' n/a ':>6}  {('%.3f' % fl) if fl is not None else ' n/a ':>6}  "
              f"{('%+.3f' % lift) if lift is not None else ' n/a ':>6}")
    a = report["all_new_families"]
    print(f"{'ALL NEW':>22}  {a['n']:>4}  {a.get('pairwise_accuracy', float('nan')):>9.3f}  "
          f"{a.get('rank_accuracy', float('nan')):>6.3f}  {a.get('majority_floor', float('nan')):>6.3f}  "
          f"{a.get('rank_lift_over_floor', float('nan')):>+6.3f}")


if __name__ == "__main__":
    main()
